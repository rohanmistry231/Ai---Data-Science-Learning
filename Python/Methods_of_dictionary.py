# dict.keys() ::
print("dict.keys() ::")
my_dict = {'collage':"darshan",'city':"rajkot",'type':"engineering"}
print(my_dict.keys())
print("")
# dict.values() ::
print("dict.values() ::")
my_dict = {'collage':"darshan",'city':"rajkot",'type':"engineering"}
print(my_dict.values())
print("")
# dict.items() ::
print("dict.items() ::")
my_dict = {'collage':"darshan",'city':"rajkot",'type':"engineering"}
print(my_dict.items())
print("")