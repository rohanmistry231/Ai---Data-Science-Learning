# while loop ::

print("While loop ::")
x = 5
while x < 3:
    print(x)
    x += 1 # x++ is invalid in python 
else :
    print("x is greater than 3\n")

# for loop ::

print("for loop ::")
my_list = [1,2,3,4]
for list_item in my_list :
    print(list_item)
my_list = range(0,6)
for list_item in my_list :
    print(list_item)
