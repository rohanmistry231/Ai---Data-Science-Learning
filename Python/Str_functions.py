# len() ::
x = 'Rohan'
print(len(x))
# count() ::
x = 'banana'
print(x.count('na'))
# title() , lower() , upper() :: 
x = 'darshan institute, rajkot'
print(x.title())
print(x.lower())
print(x.upper())
# istitle() , islower() , isupper() ::
x = 'darshan institute, rajkot'
print(x.istitle())
print(x.islower())
print(x.isupper())
# find() , replace() ::
x = 'darshan institute, rajkot'
print(x.find('in'))
print(x.replace('tute','fufe'))
# index() ::
x = 'darshan institute, rajkot'
print(x.index('in'))
# isaplha() , isalnum() , isdecimal() , isdigit() , isnumeric() , isascii() ::
x = 'darshan123'
print(x.isalpha())
print(x.isalnum())
print(x.isdecimal())
print(x.isdigit())
print(x.isnumeric())
print(x.isascii())
# strip() , lstrip() , rstrip() ::
x = '   Rohan   '
print(x.strip())
print(x.lstrip())
print(x.rstrip())