# if statement ::

print("if statement ::")
x = 10 
if x > 5 :
    print("x is greater than 5\n")
# if else statement ::

print("if else statement ::")
x = 3
if x > 5:
    print("x is greater than 5")
else :
    print("x is less than 5\n")

# if elif else statment ::

print("if elif else statement ::")
x = 10
if x > 12:
    print("x is greater than 12")
elif x > 5:
    print("x is greater than 5")
else :
    print("x is less than 5")
